-- If TextureInfo, use the texture
-- If a table of rgba values, treat as a rectangle

-- TODO: Rewrite this file to make this less bad
---@param name string | DjuiColor | TextureInfo
---@param behavior BehaviorId?
---@param model ModelExtendedId?
---@param params ItemParameters?
---@return MenuItemLink
function add_item(name, behavior, model, params)
    local is_colored_block = name.r ~= nil

    local used_params = get_default_item_params()
    local used_anim_state = 0
    if params then
        for key, value in pairs(params) do
            if used_params[key] then
                used_params[key] = value
            end
        end
        if used_params.forceAnimState then
            used_anim_state = used_params.forceAnimState
        end
    end
    ---@cast used_anim_state integer

    ---@type Item
    local item = {
        behavior = behavior or bhvMceBlock,
        model = model or (is_colored_block and E_MODEL_MCE_COLOR_BLOCK or E_MODEL_MCE_BLOCK),
        animState = used_anim_state,
        params = used_params
    }

    local texture = nil
    if name.texture then
        ---@cast name TextureInfo
        texture = name
    else
        ---@type DjuiColor | TextureInfo
        texture = is_colored_block and name --[[@as DjuiColor]] or get_texture_info(name --[[@as string]])
    end
    ---@type MenuItemLink
    local link = {item = item, icon = texture}
    return link
end

---@param name string | DjuiColor | TextureInfo
---@param behavior BehaviorId?
---@param model ModelExtendedId?
---@param params ItemParameters?
---@param anim integer
---@return MenuItemLink
function add_item_with_anim_state(name, behavior, model, params, anim)
    params.forceAnimState = nil
    return add_item(name, behavior, model, params)
end

